        'XMinorTick'  , 'off'       , ...
        'YMinorTick'  , 'on'        , ...
        'YGrid'       , 'on'        , ...
        'XColor'      , [.3 .3 .3]  , ...
        'YColor'      , [.3 .3 .3]  , ...
        'YTick'       , floor (min (min (results_Subset) ) ):1:ceil(max (max (results_Subset) ) ), ...
        'XTick'       , 1:1:5       , ...
        'XTickLabel'  , {'session #1', 'session #2', 'session #3', 'session #4', 'session #5'}, ...
        'LineWidth'   , 1           );
    
    set(gcf, 'PaperPositionMode', 'auto');
    
    if absoluteValues
